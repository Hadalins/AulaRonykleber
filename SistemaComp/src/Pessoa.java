
public class Pessoa {

	private int Id;
	private String nome;
	private int Numerotelefone;
	private   String email;
	private String RdeSocial;
	private int cep;
	private int num;
	private String CasaAp;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getNumerotelefone() {
		return Numerotelefone;
	}
	public void setNumerotelefone(int numerotelefone) {
		Numerotelefone = numerotelefone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getRdeSocial() {
		return RdeSocial;
	}
	public void setRdeSocial(String rdeSocial) {
		RdeSocial = rdeSocial;
	}
	public int getCep() {
		return cep;
	}
	public void setCep(int cep) {
		this.cep = cep;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getCasaAp() {
		return CasaAp;
	}
	public void setCasaAp(String casaAp) {
		CasaAp = casaAp;
	}
	
	
	
	
}
